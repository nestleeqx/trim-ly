'use client'

import Button from '@/app/components/ui/Button'
import { ArrowLeft, Eye, EyeOff, Github, Link as LinkIcon } from 'lucide-react'
import Link from 'next/link'
import { useCallback, useState } from 'react'
import styles from './page.module.scss'

export default function LoginPage() {
	const [showPassword, setShowPassword] = useState(false)
	const [email, setEmail] = useState('')
	const [password, setPassword] = useState('')
	const [remember, setRemember] = useState(false)

	const togglePassword = useCallback(() => {
		setShowPassword(prev => !prev)
	}, [])

	const handleSubmit = useCallback((e: React.FormEvent) => {
		e.preventDefault()
	}, [])

	return (
		<div className={styles.page}>
			{/* Logo */}
			<div className={styles.logo}>
				<div className={styles.logoIcon}>
					<LinkIcon size={24} />
				</div>
				<span className={styles.logoText}>trim.ly</span>
			</div>

			{/* Card */}
			<div className={styles.card}>
				<div className={styles.cardHeader}>
					<h1 className={styles.cardTitle}>С возвращением</h1>
					<p className={styles.cardSubtitle}>
						Войдите, чтобы управлять ссылками и аналитикой.
					</p>
				</div>

				<form
					className={styles.form}
					onSubmit={handleSubmit}
				>
					{/* Email */}
					<div className={styles.formGroup}>
						<label
							className={styles.label}
							htmlFor='email'
						>
							Email адрес
						</label>
						<input
							id='email'
							type='email'
							className={styles.input}
							placeholder='name@company.com'
							value={email}
							onChange={e => setEmail(e.target.value)}
							autoComplete='email'
						/>
					</div>

					{/* Password */}
					<div className={styles.formGroup}>
						<div className={styles.labelRow}>
							<label
								className={styles.label}
								htmlFor='password'
							>
								Пароль
							</label>
							<Link
								href='/forgot-password'
								className={styles.forgotLink}
							>
								Забыли пароль?
							</Link>
						</div>
						<div className={styles.inputWrapper}>
							<input
								id='password'
								type={showPassword ? 'text' : 'password'}
								className={styles.input}
								placeholder='••••••••'
								value={password}
								onChange={e => setPassword(e.target.value)}
								autoComplete='current-password'
							/>
							<button
								type='button'
								className={styles.passwordToggle}
								onClick={togglePassword}
								aria-label={
									showPassword
										? 'Скрыть пароль'
										: 'Показать пароль'
								}
							>
								{showPassword ? (
									<EyeOff size={18} />
								) : (
									<Eye size={18} />
								)}
							</button>
						</div>
					</div>

					{/* Remember me */}
					<div className={styles.rememberRow}>
						<input
							id='remember'
							type='checkbox'
							className={styles.checkbox}
							checked={remember}
							onChange={e => setRemember(e.target.checked)}
						/>
						<label
							htmlFor='remember'
							className={styles.rememberLabel}
						>
							Запомнить меня
						</label>
					</div>

					{/* Submit */}
					<Button
						variant='primary'
						size='lg'
						type='submit'
					>
						Войти
					</Button>

					{/* Divider */}
					<div className={styles.divider}>
						<div className={styles.dividerLine} />
						<span className={styles.dividerText}>
							Или продолжить с
						</span>
						<div className={styles.dividerLine} />
					</div>

					{/* Social */}
					<div className={styles.socialButtons}>
						<Button
							variant='ghost'
							className={styles.socialBtn}
						>
							<svg
								className={styles.socialIcon}
								viewBox='0 0 24 24'
							>
								<path
									d='M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z'
									fill='#4285F4'
								/>
								<path
									d='M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z'
									fill='#34A853'
								/>
								<path
									d='M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z'
									fill='#FBBC05'
								/>
								<path
									d='M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z'
									fill='#EA4335'
								/>
							</svg>
							Google
						</Button>
						<Button
							variant='ghost'
							className={styles.socialBtn}
						>
							<Github size={18} />
							GitHub
						</Button>
					</div>

					{/* Signup link */}
					<p className={styles.footerText}>
						Нет аккаунта?{' '}
						<Link
							href='/signup'
							className={styles.signupLink}
						>
							Зарегистрироваться
						</Link>
					</p>
				</form>
			</div>

			{/* Back */}
			<Link
				href='/'
				className={styles.backLink}
			>
				<ArrowLeft size={16} />
				Вернуться на главную
			</Link>
		</div>
	)
}
